        private void cariData(){
            try {
                st = koneksi.createStatement();
                rs = st.executeQuery("SELECT * FROM identifikasi WHERE `" 
                        + comboCari.getSelectedItem().toString() + 
                        "` LIKE '%" + textCari.getText() + "%'");

                DefaultTableModel model = new DefaultTableModel();
                model.addColumn("No.");
                model.addColumn("Nama Lengkap");
                model.addColumn("Jenis Kelamin");
                model.addColumn("Alamat Rumah");
                model.addColumn("Tanggal Lahir");
                model.addColumn("Nomor Telepon");

                int no = 1;

                model.getDataVector().removeAllElements();
                model.fireTableDataChanged();
                model.setRowCount(0);
                tabelData.setModel(model);

                while (rs.next()) {
                    Object[] data = {
                        no ++,
                        rs.getString("Nama Lengkap"),
                        rs.getString("Jenis Kelamin"),
                        rs.getString("Alamat Rumah"),
                        rs.getString("Tanggal Lahir"),
                        rs.getString("Nomor Telepon")
                    };
                    model.addRow(data); 
                }

            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error" + e.getMessage());
            }
        }

