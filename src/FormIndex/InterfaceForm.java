    private void buttonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimpanActionPerformed
        // TODO add your handling code here:
        try {
            st = koneksi.createStatement();
            if (textNamaPasien.getText().equals("") || 
                    textAlamatPasien.getText().equals("") || 
                    textLahirPasien.getText().equals("") ||
                    textTelpPasien.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Data tidak diperbolehkan kosong", "Validasi Data", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            if (!buttonLaki.isSelected() && !buttonPerempuan.isSelected()) {
            JOptionPane.showMessageDialog(null, "Pilih Jenis Kelamin Pasien", "Validasi Data", JOptionPane.INFORMATION_MESSAGE);
            return;
            }
            
            if (buttonSimpan.getText() == "Simpan") {
                String cek = "SELECT * FROM identifikasi WHERE `Nama Lengkap` = '" + textNamaPasien.getText() + "'";
                rs = st.executeQuery(cek);
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "Nama pasien ini sudah terdaftar");
                } else {
                    String selectedOption = buttonLaki.isSelected() ? "Laki-Laki" : (buttonPerempuan.isSelected() ? "Perempuan" : "");
                    
                    String sql = "INSERT INTO identifikasi VALUES ('" + textNamaPasien.getText() + 
                            "','" + selectedOption +
                            "','" + textAlamatPasien.getText() +
                            "','" + textLahirPasien.getText() +
                            "','" + textTelpPasien.getText() + "')";
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Data telah berhasil disimpan");
                    tableCleaning();
                    showData();
                }
            } else {
                //Pemicu pengubahan data
                String selectedOption = buttonLaki.isSelected() ? "Laki-Laki" : (buttonPerempuan.isSelected() ? "Perempuan" : "");
                String update = "UPDATE identifikasi SET `Jenis Kelamin` = '" + selectedOption +
                        "', `Nomor Telepon` = '" + textTelpPasien.getText() +
                        "', `Alamat Rumah` = '" + textAlamatPasien.getText() +
                        "', `Tanggal Lahir` = '" + textLahirPasien.getText() +
                        "' WHERE `Nama Lengkap` = '" + textNamaPasien.getText() + "'";
                st.executeUpdate(update);
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                tableCleaning();
                showData();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error");
        }
    }//GEN-LAST:event_buttonSimpanActionPerformed

    private void tabelDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelDataMouseClicked
        // TODO add your handling code here:
        textNamaPasien.setText(tabelData.getValueAt(tabelData.getSelectedRow(), 1).toString());
        textAlamatPasien.setText(tabelData.getValueAt(tabelData.getSelectedRow(), 3).toString());
        textLahirPasien.setText(tabelData.getValueAt(tabelData.getSelectedRow(), 4).toString());
        textTelpPasien.setText(tabelData.getValueAt(tabelData.getSelectedRow(), 5).toString());
        
        String selectedRadioButton = tabelData.getValueAt(tabelData.getSelectedRow(), 2).toString();
        if ("Laki-Laki".equals(selectedRadioButton)) {
            buttonLaki.setSelected(true);
            buttonPerempuan.setSelected(false);
        } else if ("Perempuan".equals(selectedRadioButton)) {
            buttonLaki.setSelected(false);
            buttonPerempuan.setSelected(true);
        }
        
        textNamaPasien.setEditable(false);
        buttonSimpan.setText("Ubah");
    }//GEN-LAST:event_tabelDataMouseClicked

    private void buttonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonHapusActionPerformed
        // TODO add your handling code here:
        if (textNamaPasien.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Silahkan pilih data yang akan dihapus!");
        } else {
            int Jawaban = JOptionPane.showConfirmDialog(null, "Data ini akan dihapus, Tekan Yes untuk lanjutkan", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (Jawaban == 0) {
                try {
                    st = koneksi.createStatement();
                    String jenis = buttonLaki.isSelected() ? "Laki-Laki" : "Perempuan";
                    
                    String sql = "DELETE FROM identifikasi WHERE `Nama Lengkap` = '" + textNamaPasien.getText() + "' AND `Jenis Kelamin` = '" + jenis + "'";
                    
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                    showData();
                    tableCleaning();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Error");
                }
            }
        }
    }//GEN-LAST:event_buttonHapusActionPerformed

